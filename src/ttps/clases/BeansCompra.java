package ttps.clases;

import java.util.Date;

public class BeansCompra {

	private Date dia;
	private String formaRetiro;
	private String[] menu_ids;
	
	
	public Date getDia() {
		return dia;
	}
	public void setDia(Date dia) {
		this.dia = dia;
	}
	public String getFormaRetiro() {
		return formaRetiro;
	}
	public void setFormaRetiro(String formaRetiro) {
		this.formaRetiro = formaRetiro;
	}
	public String[] getMenu_ids() {
		return menu_ids;
	}
	public void setMenu_ids(String[] menu_ids) {
		this.menu_ids = menu_ids;
	}

	
}
